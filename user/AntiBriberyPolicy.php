        <li>All countries and locations where we conduct business</li>
      </ul>
    </p>

    <h2>3. What Constitutes a Bribe?</h2>
    <p>
      A bribe is defined as any offer, promise, giving, or acceptance of a financial or other advantage with the intention of influencing an individual to act improperly. Examples include:
      <ul>
        <li>Cash or cash equivalents (e.g., gift cards)</li>
        <li>Gifts, entertainment, or hospitality that are excessive or not transparent</li>
        <li>Favors, discounts, or other forms of preferential treatment</li>
        <li>Political or charitable donations linked to obtaining business</li>
      </ul>
    </p>

    <h2>4. Gifts and Hospitality</h2>
    <p>
      GOGO_Supermarket permits reasonable and proportionate gifts and hospitality provided they:
      <ul>
        <li>Are given in good faith and not to influence any business decision</li>
        <li>Are of modest value and not frequent</li>
        <li>Comply with local laws and customs</li>
        <li>Are properly documented and reported</li>
      </ul>
      Any gift or hospitality that may be perceived as a bribe must be declined.
    </p>

